    public void ejecutarComando(Comando comando) {
        if (comando.ejecutar()) {
            this.historiaComandos.push(comando);
            
            if (!this.deshacerButton.isEnabled()) {
                this.deshacerButton.setEnabled(true);
                this.deshacerMenuItem.setEnabled(true);
            }
        }
    }
    
    public String getPortapapeles() {
        return portapapeles;
    }

    public void setPortapapeles(String portapapeles) {
        this.portapapeles = portapapeles;
    }
    
    public void deshacer() {
        this.historiaComandos.pop().deshacer();
        
        if (this.historiaComandos.isEmpty()) {
            this.deshacerButton.setEnabled(false);
            this.deshacerMenuItem.setEnabled(false);
        }
    }
    
